    public void readTable(){
                DefaultTableModel modelo = (DefaultTableModel) jprodutos.getModel();
                modelo.setNumRows(0);
                ProdutoDao pdao = new ProdutoDao();
                
                for (Produto p:pdao.read()){
                    modelo.addRow(new Object[]{
                      p.getId(),
                      p.getDescricao(),
                      p.getQtd(),
                      p.getPreco()
                });

                }
    }

