    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //remover
        if(jprodutos.getSelectedRow() != -1){
              Produto p = new Produto();
            ProdutoDao dao = new ProdutoDao();
           
            p.setId((int)jprodutos.getValueAt(jprodutos.getSelectedRow(), 0));
            dao.delete(p);

            txtdesc.setText("");
            txtqtd.setText("");
            txtprec.setText("");

            readTable();
            
        }else{
            JOptionPane.showMessageDialog(null, "Produto nao Selecionado");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jprodutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jprodutosMouseClicked
        // TODO add your handling code here:
        //selecionar
        if(jprodutos.getSelectedRow() != -1){
            txtdesc.setText(jprodutos.getValueAt(jprodutos.getSelectedRow(),1).toString());
            txtqtd.setText(jprodutos.getValueAt(jprodutos.getSelectedRow(), 2).toString());
            txtprec.setText(jprodutos.getValueAt(jprodutos.getSelectedRow(),3).toString());
        }
    }//GEN-LAST:event_jprodutosMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //atualizar
        if(jprodutos.getSelectedRow() != -1){
            Produto p = new Produto();
            ProdutoDao dao = new ProdutoDao();
            p.setDescricao(txtdesc.getText());
            p.setQtd(Integer.parseInt(txtqtd.getText()));
            p.setPreco(Double.parseDouble(txtprec.getText()));
            p.setId((int)jprodutos.getValueAt(jprodutos.getSelectedRow(), 0));
            dao.update(p);

            txtdesc.setText("");
            txtqtd.setText("");
            txtprec.setText("");

            readTable();
        
        }
    }//GEN-LAST:event_jButton3ActionPerformed

