    public void readTableForDesc(String desc){
                DefaultTableModel modelo = (DefaultTableModel) jprodutos.getModel();
                modelo.setNumRows(0);
                ProdutoDao pdao = new ProdutoDao();
                
                for (Produto p:pdao.readForDesc(desc)){
                    modelo.addRow(new Object[]{
                      p.getId(),
                      p.getDescricao(),
                      p.getQtd(),
                      p.getPreco()
                });

                }
    }
